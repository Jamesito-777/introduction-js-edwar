// El operador ternario en JS es una forma corta y elegante de una estructura if...else.

// Se llama ternario por que tiene 3 operadores, obeservemos la sintaxis
// condicion ? valorSiVerdadero : valorSiFalso
// El "?" funciona como un "entonces" y el ":" significa "sino"
// Ejemplo1

let age = 18;
let message = age >= 18 ? "Eres mayor de edad" : "Eres menor de edad"
